# to convert list into tuples
my_list = [1, 2, 3, 4, 5]
my_tuple = tuple(my_list)
print(my_tuple)

# to convert string into tuple
my_string ="Hello"
my_tuple = tuple(my_string)
print(my_tuple)

# to convert tuple into string
my_tuple = ('H', 'e', 'l', 'l', 'o')

my_string =''.join(my_tuple)
print(my_string)